    {
        // add comment to a Post

        
        // validate before posting

        $this->validate(request(), [

            'body'  => 'required'
        ]);

        //$post->addComment(request('body'));

        (new Comment)->post()->associate($post)
            ->user()->associate(auth()->user())
            ->fill(request(['body']))
            ->save();

        return back();

    }

